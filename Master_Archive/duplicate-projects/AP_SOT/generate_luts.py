def format_array(name, values, precision=6):
    """Format array for C++ constexpr declaration"""
    lines = [f"constexpr float {name}[96] = {{"]
    
    for i in range(0, len(values), 4):  # 4 values per line
        chunk = values[i:i+4]
        formatted_values = [f"{val:.{precision}f}f" for val in chunk]
        line = "    " + ", ".join(formatted_values)
        if i + 4 < len(values):
            line += ","
        lines.append(line)
    
    lines.append("};")
    return "\n".join(lines)

