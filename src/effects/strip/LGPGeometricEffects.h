#ifndef LGP_GEOMETRIC_EFFECTS_H
#define LGP_GEOMETRIC_EFFECTS_H

// Light Guide Plate Geometric Pattern Effects
// Advanced shapes beyond boxes - exploiting LGP optics for unique geometries

// Diamond Lattice - Creates diamond/rhombus patterns
// Based on crossing diagonal wave interference
void lgpDiamondLattice();

// Hexagonal Grid - Honeycomb patterns using 3-wave interference
// Like acoustic cymatics patterns
void lgpHexagonalGrid();

// Spiral Vortex - Rotating spiral patterns
// Based on optical vortex beam theory
void lgpSpiralVortex();

// Sierpinski Triangles - Fractal patterns
// Self-similar interference at multiple scales
void lgpSierpinskiTriangles();

// Chevron Waves - V-shaped moving patterns
// Like wake patterns in water
void lgpChevronWaves();

// Concentric Rings - Radial standing waves
// Based on Bessel functions in waveguides
void lgpConcentricRings();

// Star Burst - Radiating star patterns
// Multiple radial waves with angular modulation
void lgpStarBurst();

// Mesh Network - Interconnected nodes
// Simulates network topology visualization
void lgpMeshNetwork();

#endif // LGP_GEOMETRIC_EFFECTS_H